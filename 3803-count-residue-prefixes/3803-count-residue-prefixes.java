class Solution {
    public int residuePrefixes(String s) {
        int count=0;
        HashSet<Character> set = new HashSet<>();
        for(int i =0;i<s.length();i++){
            
            set.add(s.charAt(i));

            if((set.size()) == (i+1)%3)
                count++;

            if(set.size() > 3) 
                return count; 
        }

        return count;
    }
}